function addElements(container: Element) {
  const vignettePaths = [
    "1.jpg",
    "2.jpg",
    "3.jpg",
    "4.jpg",
    "5.jpg",
    "6.jpg",
    "7.jpg",
  ];

  vignettePaths.forEach((path, i) => {
    const elem = document.createElement("div");
    // use linked class as that's generated by Cargo
    elem.classList.add("vignette", "linked");
    elem.id = `${++i}`;
    elem.innerHTML = `
        <img src="/images/${path}">
    `;
    container.append(elem);
  });
}
